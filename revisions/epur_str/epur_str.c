#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	espace;

	if (ac == 2)
	{
		i = 0;
		while (av[1][i] == 32 || av[1][i] == '\t')
			i++;
		while (av[1][i])
		{
			if (av[1][i] == 32 || av[1][i] == '\t')
				espace = 1;
			if (!(av[1][i] == 32 || av[1][i] == '\t'))
			{
				if (espace)
					write(1, " ", 1);
				espace = 0;
				write(1, &av[1][i], 1);
			}
			i++;
		}

	}
	write(1, "\n", 1);
}
