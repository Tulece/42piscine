int	ft_atoi(char *str)
{
	int	result;
	int	sign;
	int	i;
	int	nb_sign;

	result = 0;
	sign = 1;
	i = 0;
	nb_sign = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			nb_sign++;
		i++;
	}
	if (nb_sign % 2 != 0)
		sign = -1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - '0';
		i++;
	}
	return (result * sign);
}
